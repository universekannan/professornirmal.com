                                <h2 class="title">Teaching</h2>
                                
                                <div class="row">
                                    <div class="col-md-12">
                                        <p style="text-align:justify">The contagious energy  share in the classroom brings new ideas and clarity of thought to my research. But the aspect of teaching I have come to cherish the most is the feeling of restoring hope and giving direction to struggling students.<br/><br/>
                                        My biggest challenge is to inspire students to focus on studying our course material by explaining first-hand its practical benefits with clear, real-life examples and case studies. Many students feel lost, swept up in searching for their place in society and purpose in life, and this at-times overwhelming philosophical venture can easily dampen their enthusiasm for the learning experience. To keep them grounded and on-track for success, I balance lecture with discussion and group work.<br/><br/>
                                         Our society can improve the lives of future generations by better preparing them for holistic success. This effort demands refining our pedagogy, and ultimately our education system. I therefore hope to continue teaching, as well as advancing personal research projects, to contribute to social progress and the betterment of our world.
</p>
